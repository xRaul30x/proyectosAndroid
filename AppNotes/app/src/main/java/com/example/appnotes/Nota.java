package com.example.appnotes;

public class Nota {

    int id;
    String titulo;
    String cuerpo;
    String color;

    public Nota(int id, String titulo, String cuerpo, String color){
        this.id = id;
        this.titulo = titulo;
        this.cuerpo = cuerpo;
        this.color = color;
    }
}
